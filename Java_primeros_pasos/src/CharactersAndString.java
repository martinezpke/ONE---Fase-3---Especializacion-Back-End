
public class CharactersAndString {
	public static void main(String[] args) {
		// characters 
		char caracter = 'a';
		System.out.println(caracter);
		
		caracter = 65;
		System.out.println(caracter);
		
		caracter = 65 + 1;
		char SegundaVaribleChar = (char) (caracter + 1);
		System.out.println(SegundaVaribleChar);
		
		// string
		
		String saludo = "Hola, como estas?";
		System.out.println(saludo);
	}	
}
